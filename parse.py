for r in raw.split('\n'):
    if not r:
        continue
    terms = r.split()
    if is_prefix(terms[0]):
        if line:
            lines.append(line)
        ts = [term for term in terms if not is_prefix(term) and term not in (
            '本类包括',
            '本门类包括',
            '大类',
            )]
        if len(ts) > 1:
            if ts[1].startswith('大类，'):
                ts[1] = ts[1].replace('大类，', '')
            line = '%s --%s' % (ts[0], ' '.join(ts[1:]))
        else:
            line = ' '.join(ts)
        line = '\t' * len(terms[0]) + line
    else:
        line += r.strip()
if line:
    lines.append(line)
print('\n'.join(lines))

